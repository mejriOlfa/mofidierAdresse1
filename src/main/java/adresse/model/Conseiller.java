package adresse.model;

public class Conseiller {
 private String id;
 private String nom;
 private String canal;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getNom() {
	return nom;
}
public void setNom(String nom) {
	this.nom = nom;
}
public String getCanal() {
	return canal;
}
public void setCanal(String canal) {
	this.canal = canal;
}
 
 
}
